# simple_python1
# Demonstrates variables, comments, arithmetic operators, and type identification

a = 10         # integer variable
b = 3.5        # float variable
name = "Sam"   # string variable

# arithmetic operations
sum_ab = a + b
diff = a - b
prod = a * b
quot = a / b     # float division
floor = a // b   # floor division
pow_ab = a ** 2  # exponentiation
mod = a % 3      # modulus

# print values and their data types
print("Name:", name, "->", type(name))
print("a =", a, "->", type(a))
print("b =", b, "->", type(b))
print("sum_ab =", sum_ab, "->", type(sum_ab))
print("quot =", quot, "->", type(quot))
print("floor =", floor, "->", type(floor))
print("pow_ab =", pow_ab, "->", type(pow_ab))
print("mod =", mod, "->", type(mod))